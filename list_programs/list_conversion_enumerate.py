# Conversion and Iterator Functions (Beginner Friendly)

# List declaration
l1 = [3, 4, 8, 0, 8, 8]

#convert Tuple to list
s = ("deepak", "singh", "rajput")
print("Tuple to List:", list(s))

#reverse List
print("Reversed List:", list(reversed(l1)))

# Enumerate example
list1 = ["apple", "banana", "mango"]

print("Enumerate Output:", list(enumerate(list1)))